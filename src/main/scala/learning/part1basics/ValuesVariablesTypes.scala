package learning.part1basics

object ValuesVariablesTypes extends App {

  // VALS ARE IMMUTABLE :O
  // PREFER VALS OVER VARS
  val x: Int = 42
  println(x)

  val aString: String = "hi"
  val aChar: Char = 'a'
  val anInt: Int = 44
  val aShort: Short = 32767 //max value
  val aDouble: Double = 6.4
  val aBoolean: Boolean = true

  // VAR ARE MUTABLE
  var y: Int = 44

}
